
public class BowTie {



	String color;

	String size;

	double price;

	String image;



public BowTie(String c, String s, double p, String i)

{

color = c;

size = s;

price = p;

image = i;

}



	public String getColor() {

		return color;

	}



	public void setColor(String color) {

		this.color = color;

	}



	public String getSize() {

		return size;

	}



	public void setSize(String size) {

		this.size = size;

	}



	public String getImage() {

		return image;

	}



	public void setImage(String image) {

		this.image = image;

	}



	public double getPrice() {

		return price;

	}



	public void setPrice(double price) {

		this.price = price;

	}



}

